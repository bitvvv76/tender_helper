def analyze_tender_simple(tender):
    title = tender.get("title", "Без названия")
    region = tender.get("region", "Регион не указан")
    price = tender.get("price", 0)
    customer = tender.get("customer", "Заказчик не указан")

    price_text = f"{price:,} ₽".replace(",", " ")

    risks = []

    if price >= 5_000_000:
        risks.append("крупная сумма закупки — нужно внимательно проверить требования и обеспечение")
    elif price <= 1_000_000:
        risks.append("небольшой бюджет — важно оценить, хватит ли суммы на выполнение работ")
    else:
        risks.append("бюджет выглядит средним, но всё равно нужно проверить смету и сроки")

    if price >= 10_000_000:
        risks.append(
            "крупный тендер — стоит проверить размер обеспечения заявки и исполнения контракта"
        )

    if "администрация" in customer.lower():
        risks.append(
            "заказчик является органом власти — важно внимательно проверить комплект документов"
        )

    if "школ" in title.lower():
        risks.append(
            "работы связаны с образовательным учреждением — обратите внимание на сроки выполнения"
        )

    if "детск" in title.lower():
        risks.append(
            "работы связаны с детским учреждением — могут быть дополнительные требования к безопасности"
        )

    if "ремонт" in title.lower():
        risks.append("по ремонтным работам важно проверить техническое задание и объём работ")

    if "поставка" in title.lower():
        risks.append("по поставке важно проверить сроки, характеристики товара и условия доставки")

    if "строительство" in title.lower():
        risks.append("по строительству могут быть дополнительные требования к опыту и документам")
    # Простая оценка привлекательности тендера

    if price < 1_000_000:
        rating = "⭐⭐☆☆☆"
    elif price < 5_000_000:
        rating = "⭐⭐⭐☆☆"
    elif price < 10_000_000:
        rating = "⭐⭐⭐⭐☆"
    else:
        rating = "⭐⭐⭐⭐⭐"

    result = (
        f"🤖 AI-анализ демо-тендера\n\n"
        f"📌 Тендер: {title}\n"
        f"📍 Регион: {region}\n"
        f"💰 Цена: {price_text}\n"
        f"🏢 Заказчик: {customer}\n\n"
        f"Простыми словами:\n"
        f"Это закупка, где заказчику нужен подрядчик или поставщик по теме: {title.lower()}.\n\n"
        f"На что обратить внимание:\n"
    )

    for index, risk in enumerate(risks, start=1):
        result += f"{index}. {risk}\n"

    result += (
        f"\nПредварительная оценка:\n"
        f"{rating}\n\n"
        f"Предварительный вывод:\n"
        f"Тендер можно рассматривать дальше, если компания подходит по опыту, "
        f"региону, бюджету и срокам выполнения.\n\n"
        f"Важно: это демо-анализ для MVP. Позже сюда можно подключить настоящий AI-анализ."
    )

    return result